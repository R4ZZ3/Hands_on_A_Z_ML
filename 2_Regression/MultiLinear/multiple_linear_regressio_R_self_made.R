# Multiple Linear Regression


#Importing the dataset
dataset = read.csv("50_Startups.csv")
dataset = dataset[, 2:3]

# Encoding categorical data
dataset$State = factor(dataset$State,
                         levels = c('New York', 'California', 'Florida'),
                         labels = c(1, 2, 3))


#Splitting the dataset into the Training set and Test set
#install.packages("caTools")
#fgtlibrary(caTools) tälleenkin voi ottaa kirjaston käyttöön tai Packages osiosta
set.seed(123)
split = sample.split(dataset$Profit, SplitRatio = 0.8)
training_set = subset(dataset, split == TRUE)
test_set = subset(dataset, split == FALSE)

# Feature Scaling  #Ei tarvii koska funktio ottaa haltuun multiplessa
# training_set[,2:3]= scale(training_set[,2:3])
# test_set[,2:3] = scale(test_set[,2:3])

#Fitting Multiple Linear Regression to the Training set
regressor = lm(formula = Profit ~ ., #. tarkoittaa lineaariyhdistämistä kaikista muista muuttujista)
               data = training_set)

#Predicting the Test set results
y_pred = predict(regressor, newdata = test_set)

#Building the optimal model using Backward Elimination
regressor = lm(formula = Profit ~ R.D.Spend + Administration + Marketing.Spend + State, #. tarkoittaa lineaariyhdistämistä kaikista muista muuttujista)
               data = dataset)

summary(regressor)

